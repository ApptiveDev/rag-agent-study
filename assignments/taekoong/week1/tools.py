@tool
def get_item_info(item_name: str) -> str:
    """마인크래프트 아이템 또는 블록의 상세 정보를 조회합니다.
    아이템 종류, 획득 방법, 주요 용도를 반환합니다.

    Args:
        item_name: 조회할 아이템 또는 블록 이름 (예: '다이아몬드', '흑요석', '활')
    """
    key = item_name.strip()
    data = ITEM_DB.get(key)
    if data is None:
        available = ", ".join(ITEM_DB.keys())
        return f"'{key}' 정보를 찾을 수 없습니다. 조회 가능 목록: {available}"

    return (
        f"[아이템 정보: {key}]\n"
        f"종류: {data['type']}\n"
        f"설명: {data['description']}\n"
        f"획득 방법: {data['obtain']}\n"
        f"용도: {data['uses']}"
    )


@tool
def get_crafting_recipe(item_name: str) -> str:
    """마인크래프트 아이템의 제작(크래프팅) 레시피를 조회합니다.
    필요 재료, 배치 패턴, 생산 수량을 반환합니다.

    Args:
        item_name: 레시피를 조회할 아이템 이름 (예: '다이아몬드 검', '횃불', '황금 사과')
    """
    key = item_name.strip()
    data = RECIPE_DB.get(key)
    if data is None:
        available = ", ".join(RECIPE_DB.keys())
        return f"'{key}' 레시피를 찾을 수 없습니다. 조회 가능 목록: {available}"

    table = "필요" if data["table_needed"] else "불필요 (인벤토리 제작 가능)"
    return (
        f"[제작 레시피: {key}]\n"
        f"재료: {', '.join(data['ingredients'])}\n"
        f"패턴:\n{data['pattern']}\n"
        f"생산량: {data['output']}\n"
        f"제작 테이블: {table}\n"
        f"참고: {data['notes']}"
    )


